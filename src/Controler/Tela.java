    public void setHero(Hero hero) {
    this.hero = hero;
    this.atualizaCamera(); //já atualiza a câmera assim que o hero for setado
}
    private int contarChips() {
    int cont = 0;
    for (Personagem p : faseAtual) {
        if (p instanceof ChipColetavel) {
            cont++;
        }
    }
    return cont;
}
    public void perderVida() {
    if (gameOver) return; // evita loop
    vidas--;

    if (vidas <= 0) {
        gameOver = true;
        JOptionPane.showMessageDialog(this, "GAME OVER");
        System.exit(0);
    }
}



